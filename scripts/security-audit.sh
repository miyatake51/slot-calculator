#!/usr/bin/env bash
# =============================================================================
# security-audit.sh
#   公開前セキュリティ監査ライン（二値ゲート）
#
# 設計思想:
#   - 「複雑性の減算」に基づき、検出ツールはコア3点に限定する。
#   - 脆弱性検出ロジックは自作しない。実績あるツールに外部化する。
#   - 一つでもクリティカル検出があれば非ゼロ終了し、後続のデプロイを止める。
#
# このスクリプトが検出するもの（守備範囲）:
#   1. gitleaks    : APIキー・トークン等のシークレットがコミットに混入していないか
#   2. osv-scanner : 依存パッケージに既知の脆弱性(CVE)がないか
#   3. semgrep     : XSS・インジェクション等の典型的なコードパターン(SAST)
#
# このスクリプトが検出【できない】もの（限界。過信は禁物）:
#   - 設計レベルの論理的欠陥（例: 認可ロジックの穴、権限設計の誤り）
#   - 未知の脆弱性（ゼロデイ、データベース未登録のもの）
#   - ビジネスロジック固有の不正経路
#   ※ これらは人間のレビュー(クロスレビュー)が引き続き担う領域である。
#
# 前提:
#   - 3ツールが事前にインストールされていること。
#     ローカル(Mac): brew install gitleaks osv-scanner semgrep
#     CI:            ワークフロー側で導入（同梱の security-audit.yml を参照）
#   - 未インストールのツールはスキップされ、その旨を警告表示する。
#     （CIでは全ツール導入を前提とするため、スキップは原則発生しない）
# =============================================================================

set -uo pipefail

# --- 設定 -------------------------------------------------------------------
# 監査対象のルートディレクトリ。引数で上書き可能（既定: カレント）
TARGET_DIR="${1:-.}"

# semgrep のルールセット。auto は言語を自動判定する。
# 厳密な再現性が必要になった段階で、固定ルールセット（例: p/javascript,
# p/react, p/typescript, p/secrets）へ切り替えることを推奨する。
SEMGREP_CONFIG="auto"

# --- 表示ユーティリティ -----------------------------------------------------
if [ -t 1 ]; then
  C_RED=$'\033[31m'; C_GREEN=$'\033[32m'; C_YELLOW=$'\033[33m'
  C_BOLD=$'\033[1m'; C_RESET=$'\033[0m'
else
  C_RED=""; C_GREEN=""; C_YELLOW=""; C_BOLD=""; C_RESET=""
fi

section() { printf '\n%s=== %s ===%s\n' "$C_BOLD" "$1" "$C_RESET"; }
ok()      { printf '%s[OK]%s %s\n'   "$C_GREEN"  "$C_RESET" "$1"; }
ng()      { printf '%s[NG]%s %s\n'   "$C_RED"    "$C_RESET" "$1"; }
warn()    { printf '%s[警告]%s %s\n' "$C_YELLOW" "$C_RESET" "$1"; }

# 監査全体の合否。0=合格, 1=不合格（クリティカル検出あり）
OVERALL_RESULT=0

# 指定コマンドが存在するか
has() { command -v "$1" >/dev/null 2>&1; }

# =============================================================================
# 1. シークレット検出 (gitleaks)
# =============================================================================
section "1/3 シークレット検出 (gitleaks)"
if has gitleaks; then
  if gitleaks detect --source "$TARGET_DIR" --no-banner --redact; then
    ok "シークレットの混入は検出されなかった。"
  else
    ng "シークレット（APIキー・トークン等）の混入が検出された。"
    ng "→ 該当値を即座に無効化(ローテーション)し、履歴からも除去すること。"
    OVERALL_RESULT=1
  fi
else
  warn "gitleaks 未インストールのためスキップ（brew install gitleaks）。"
fi

# =============================================================================
# 2. 依存関係の脆弱性 (osv-scanner)
# =============================================================================
section "2/3 依存関係の脆弱性 (osv-scanner)"
if has osv-scanner; then
  # 終了コード: 0=脆弱性なし / 1=脆弱性あり / 128=スキャン対象なし
  osv-scanner scan --recursive "$TARGET_DIR"; osv_exit=$?
  if [ "$osv_exit" -eq 0 ]; then
    ok "既知の脆弱性を持つ依存パッケージは検出されなかった。"
  elif [ "$osv_exit" -eq 128 ]; then
    warn "スキャン対象の依存マニフェスト（package.json 等）が無いためスキップ。"
  else
    ng "既知の脆弱性(CVE)を持つ依存パッケージが検出された。"
    ng "→ 該当パッケージを修正版へ更新すること。"
    OVERALL_RESULT=1
  fi
else
  warn "osv-scanner 未インストールのためスキップ（brew install osv-scanner）。"
fi

# =============================================================================
# 3. 静的解析 / SAST (semgrep)
# =============================================================================
section "3/3 静的コード解析 (semgrep)"
if has semgrep; then
  if semgrep scan --config "$SEMGREP_CONFIG" --error "$TARGET_DIR"; then
    ok "危険なコードパターンは検出されなかった。"
  else
    ng "危険なコードパターン（XSS・インジェクション等）が検出された。"
    ng "→ 上記指摘箇所を修正すること。"
    OVERALL_RESULT=1
  fi
else
  warn "semgrep 未インストールのためスキップ（brew install semgrep）。"
fi

# =============================================================================
# 総合判定
# =============================================================================
section "総合判定"
if [ "$OVERALL_RESULT" -eq 0 ]; then
  ok "監査合格。デプロイを許可する。"
  echo "（注: 自動監査は設計上の欠陥や未知の脆弱性は検出しない。最終判断は人間が行うこと）"
else
  ng "監査不合格。クリティカルな問題が検出された。デプロイを中止する。"
fi

exit "$OVERALL_RESULT"
